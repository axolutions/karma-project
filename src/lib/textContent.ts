// Function to get a summary of HTML content
export function getContentSummary(html: string, maxLength: number = 150): string {
  // Strip HTML tags
  const plainText = html.replace(/<[^>]*>?/gm, '');
  
  // Limit to maxLength characters
  if (plainText.length <= maxLength) {
    return plainText;
  }
  
  // Find a good breakpoint (space)
  let breakpoint = plainText.lastIndexOf(' ', maxLength);
  if (breakpoint === -1) breakpoint = maxLength;
  
  return plainText.substring(0, breakpoint) + '...';
}
